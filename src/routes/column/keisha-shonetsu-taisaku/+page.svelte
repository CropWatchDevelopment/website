	<h2>暑熱ストレスとは</h2>
	<p>
		鶏は汗腺が発達しておらず、暑さに弱い動物です。高温多湿の環境が続くと、飼料の摂取量や産卵率が下がり、増体にも影響します。特に、気温が急に上がる日や、夜間に温度が下がりきらない日は注意が必要です。被害は鶏舎の場所によっても差が出るため、<strong>どこがどれだけ暑いのか</strong>を把握することが対策の出発点になります。
	</p>

	<h2>暑熱対策の基本</h2>
	<p>一般的な暑熱対策には、次のようなものがあります。</p>
	<ul>
		<li><strong>換気</strong>：ファンや入気口を調整し、熱と湿気を外へ逃がす</li>
		<li><strong>送風</strong>：鶏に直接風を当てて体感温度を下げる</li>
		<li><strong>飲水管理</strong>：新鮮で冷たい水をいつでも飲めるようにする</li>
		<li><strong>遮熱・細霧</strong>：屋根の遮熱や細霧冷房で鶏舎内の温度上昇を抑える</li>
	</ul>
	<p>
		どの対策も、「いつ・どれだけ暑くなっているか」が分からなければ、判断が後手に回ります。
	</p>

