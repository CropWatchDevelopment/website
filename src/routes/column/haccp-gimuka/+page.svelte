	<h2>HACCP義務化とは</h2>
	<p>
		HACCPは、食品の製造や調理の工程で危害（食中毒の原因など）を予測し、重要な工程を継続的に管理する衛生管理の手法です。2018年の食品衛生法改正を受け、2021年6月1日から原則すべての食品等事業者にHACCPに沿った衛生管理が求められるようになりました。
	</p>
	<p>
		小規模な飲食店などは「HACCPの考え方を取り入れた衛生管理」として、業種別の手引書に沿って取り組めば十分とされています。難しく考える必要はありませんが、<strong>日々の衛生管理を「記録して残す」</strong>という点が、これまでと大きく変わったところです。
	</p>

	<h2>温度管理が重要な理由</h2>
	<p>
		食中毒の多くは、細菌の増殖と関係しています。細菌が増えやすい温度帯を避けるため、冷蔵庫・冷凍庫を適切な温度に保ち、その状態を確認・記録することが衛生管理の基本になります。冷蔵庫の故障や扉の閉め忘れに早く気づけるかどうかが、品質を守る分かれ目です。
	</p>

	<h2>まず何から始めるか</h2>
	<p>最初の一歩は、次の3つです。</p>
	<ul>
		<li>冷蔵庫・冷凍庫ごとに、保つべき温度の基準を決める</li>
		<li>1日に決めた回数、温度を確認する</li>
		<li>確認した温度と、異常があったときの対応を記録して保存する</li>
	</ul>
	<p>
		紙のチェック表でも対応はできますが、確認のたびに温度計を見て手書きする運用は、記録漏れや転記ミスが起こりやすく、担当者の負担にもなります。
	</p>

	<h2>記録の自動化という選択肢</h2>
	<p>
		そこで広がっているのが、IoTの温度センサーによる<strong>温度記録の自動化</strong>です。冷蔵庫・冷凍庫にセンサーを置くだけで温度が自動で記録され、基準を外れたときにはメールなどで通知されます。監査のときも、保存された記録をそのまま提出できます。
	</p>
